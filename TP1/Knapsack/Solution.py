
class Solution:
    def __init__(self,instance,valeur):
        self.instance = instance
        self.valeur = valeur
        
    def setInstance(self,instance):
        self.instance = instance
    
    def setValeur(self,valeur):
        self.valeur = valeur
    
    def getInstance(self):
        return self.instance
    
    def getValeur(self):
        return self.valeur
    
